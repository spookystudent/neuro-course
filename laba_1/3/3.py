# 1) Создание переменных всех типов Python

# Целочисленный тип
integer_var = 10

# Числа с плавающей точкой
float_var = 3.14

# Строковый тип
string_var = "Привет, мир!"

# Логический тип
boolean_var = True

# Список
list_var = [1, 2, 3, 4, 5]

# Кортеж
tuple_var = (1, 2, 3, 4, 5)

# Словарь
dict_var = {"ключ1": "значение1", "ключ2": "значение2"}

# Множество
set_var = {1, 2, 3, 4, 5}

# 2) Действия с переменными

# Целочисленный тип
integer_var += 5

# Числа с плавающей точкой
float_var *= 2

# Строковый тип
string_var += " Добро пожаловать!"

# Логический тип
boolean_var = not boolean_var

# Список
list_var.append(6)

# Кортеж
tuple_var += (6,)

# Словарь
dict_var["ключ3"] = "значение3"

# Множество
set_var.add(6)

# 3) Вывод значений переменных с комментарием о типе

print(f"Целочисленный тип: {integer_var}")
print(f"Числа с плавающей точкой: {float_var}")
print(f"Строковый тип: {string_var}")
print(f"Логический тип: {boolean_var}")
print(f"Список: {list_var}")
print(f"Кортеж: {tuple_var}")
print(f"Словарь: {dict_var}")
print(f"Множество: {set_var}")
